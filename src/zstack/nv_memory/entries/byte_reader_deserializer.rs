    fn deserialize_u8<V>(self, _visitor: V) -> Result<V::Value, Self::Error>
    where
        V: serde::de::Visitor<'de>,
    {
        let mut buf = [0u8; 1];
        self.reader
            .read_exact(&mut buf)
            .map_err(|_| ByteReaderError::custom("Failed to read u8"))?;
        _visitor.visit_u8(buf[0])
    }

    fn deserialize_u16<V>(self, _visitor: V) -> Result<V::Value, Self::Error>
    where
        V: serde::de::Visitor<'de>,
    {
        let mut buf = [0u8; 2];
        self.reader
            .read_exact(&mut buf)
            .map_err(|_| ByteReaderError::custom("Failed to read u8"))?;
        _visitor.visit_u16(u16::from_be_bytes(buf))
    }

